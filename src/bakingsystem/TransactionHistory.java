     public void Table1(){
        String userName="";
        try {
            File file;
            file = new File("text.txt");
            Scanner sc = new Scanner(file);
            userName=sc.next();
            sc.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(CustomerDashboard.class.getName()).log(Level.SEVERE, null, ex);
        }
        Vector vctData = new Vector();
        Vector vctHeader = new Vector();
        vctHeader.add("Date");
        vctHeader.add("Time");
        vctHeader.add("Type");
        vctHeader.add("Amount");

        String sql="select * from TranHis where UserName=?";
        try{
            pst=conn.prepareStatement(sql);
            pst.setString(1, userName);
            rs=pst.executeQuery();
            while(rs.next()){
                Vector vec = new Vector();
                vec.add(rs.getString("Date"));
                vec.add(rs.getString("Time"));
                vec.add(rs.getString("Type"));
                vec.add(rs.getString("Amount"));
                vctData.add(vec);
            }
            jTable2.setModel(new DefaultTableModel(vctData, vctHeader));
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }finally{
            try{
                rs.close();
                pst.close();
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }

